peso1 = float(input("Digite o peso da primeira pessoa em kg: "))
peso2 = float(input("Digite o peso da segunda pessoa em kg: "))

print("-------------------------------------------------------")

if peso1 > peso2:
    print("A primeira pessoa é mais pesada do que a segunda pessoa")
elif peso1 == peso2:
    print("As pessoas tem os mesmos pesos")
else:
    print("A segunda pessoa é mais pesada do que a primeira pessoa")




